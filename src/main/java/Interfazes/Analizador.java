    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (this.jTextArea1.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "No hay texto para analizar");
        } else {
            AlmacenadorInfo.getDatosTabla().clear();
            String texto = this.jTextArea1.getText();
            System.out.println(texto);
            token = new DeterminadorToken(texto);
            while (token.getPosicion() < texto.length()) {
                token.separarTexto();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

