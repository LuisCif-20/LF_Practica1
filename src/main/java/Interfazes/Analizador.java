    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nombre = JOptionPane.showInputDialog("Ingresa el nombre de tu archivo") + ".txt";
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No ingresaste un nombre");
        } else {
            JFileChooser fc = new JFileChooser();
            //Indicamos que solo la ruta de los directorios jale
            fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            int seleccion = fc.showOpenDialog(this);
            // Escogemos el fichero
            if (seleccion == JFileChooser.APPROVE_OPTION) {
                String rutaAbsoluta = fc.getSelectedFile() + File.separator + nombre;
                LectorArchivo.setRuta(rutaAbsoluta);
                LectorArchivo.guardarCambios(jTextArea1);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

